#!/usr/bin/env python3
"""
notes_to_cortex.py — Ingest Apple Notes + knowledge files into Cortex memory.

Usage:
  python3 scripts/notes_to_cortex.py [--dry-run] [--skip-knowledge]

What it does:
  1. Reads data/notes_index.json (generated by notes_indexer.py --index)
  2. Categorizes each note by keyword rules
  3. POSTs every valid note to Cortex via /remember
  4. Bootstraps Cortex from ALL existing knowledge/ files:
     - knowledge/products/*.md          → product_reference
     - knowledge/projects/*/system_shell.md → system_shell
     - knowledge/procedures/*.md        → work_procedure
     - knowledge/sow-blocks/*.md        → proposal_template
     - knowledge/proposal_library/**/*.md → proposal_template
     - knowledge/hardware/*.md          → work_procedure / product_reference
     - knowledge/network/*.md           → work_procedure / infrastructure
     - knowledge/incidents/*.md         → troubleshooting
     - knowledge/operations-runbook.md  → work_procedure
     - knowledge/reports/*.md           → business_operations
     - knowledge/cortex/learnings.md    → meta_learning
     - knowledge/agents/*.md            → infrastructure
  5. Builds knowledge/product_reference.md rollup
  6. Writes data/notes_ingest_report.md
"""

from __future__ import annotations

import argparse
import hashlib
import json
import logging
import os
import re
import sys
from collections import defaultdict
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

import httpx

REPO_ROOT = Path(__file__).resolve().parents[1]
DATA_DIR = REPO_ROOT / "data"
KNOWLEDGE_DIR = REPO_ROOT / "knowledge"
NOTES_INDEX = DATA_DIR / "notes_index.json"
INGEST_REPORT = DATA_DIR / "notes_ingest_report.md"
PRODUCT_REF_MD = KNOWLEDGE_DIR / "product_reference.md"
PROCEDURES_DIR = KNOWLEDGE_DIR / "procedures"

CORTEX_URL = os.environ.get("CORTEX_URL", "http://localhost:8102")
SEEN_HASHES_FILE = DATA_DIR / "notes_ingest_seen_hashes.json"

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s [%(levelname)s] %(name)s — %(message)s",
)
logger = logging.getLogger("notes_to_cortex")

# ── Seen-hash dedupe ──────────────────────────────────────────────────────────

def _load_seen() -> set[str]:
    if SEEN_HASHES_FILE.is_file():
        try:
            return set(json.loads(SEEN_HASHES_FILE.read_text()))
        except Exception:
            pass
    return set()


def _save_seen(hashes: set[str]) -> None:
    SEEN_HASHES_FILE.write_text(json.dumps(sorted(hashes), indent=2))


def _content_hash(text: str) -> str:
    return hashlib.sha256(text.strip().encode()).hexdigest()[:16]


# ── Categorization ────────────────────────────────────────────────────────────

# (pattern, category, importance)
_RULES: list[tuple[re.Pattern, str, int]] = [
    (re.compile(r"\bVLAN\b|subnet|10\.\d+\.\d+|device registry|switch port|cable schedule", re.I), "system_shell", 9),
    (re.compile(r"\bwifi\b|\bssid\b|password|alarm code|gate code|\bpin\b|passcode", re.I), "access_codes", 10),
    (re.compile(r"step 1|step 2|how to|procedure|instructions|cheat sheet", re.I), "work_procedure", 8),
    (re.compile(r"\bmodel\b|part number|\bSKU\b|specs|compatibility|pricing|msrp", re.I), "product_reference", 7),
    (re.compile(r"proposal|scope|agreement|quote|pricing template|deliverables", re.I), "proposal_template", 8),
    (re.compile(r"client prefer|likes|dislikes|decided on|chose|communication style", re.I), "client_preference", 7),
    (re.compile(r"job site|installed|ran wire|mounted|field note|on site|in the field", re.I), "install_notes", 6),
    (re.compile(r"\bfix\b|solved|issue was|workaround|\bdebug\b|troubleshoot|problem:", re.I), "troubleshooting", 8),
    (re.compile(r"\brep\b|supplier|vendor|account number|distributor|dealer", re.I), "vendor_contact", 6),
    (re.compile(r"certification|\bexam\b|training|study|CEDIA|Control4 cert|Lutron cert", re.I), "training", 5),
    (re.compile(r"schedule|inventory|truck stock|process|business ops|operations", re.I), "business_operations", 5),
]


def categorize(title: str, body: str, folder: str) -> tuple[str, int]:
    """Return (category, importance) for a note."""
    blob = f"{title}\n{body}\n{folder}"
    for pattern, cat, imp in _RULES:
        if pattern.search(blob):
            return cat, imp
    return "install_notes", 5  # catch-all for unmatched notes


# ── Tag extraction ────────────────────────────────────────────────────────────

_CLIENT_NAMES = ["Topletz", "Gates", "Hernaiz", "Kelly", "Aspen Meadow", "Arrowhead", "Bersin",
                 "Burillo", "Rinn", "Miller", "Peck", "Staten", "Clark", "Foy", "Anderson",
                 "Spencer", "Minervini", "Alizadeh", "Hukill", "Ruzika", "Jones", "Coleman"]
_BRANDS = ["Control4", "Lutron", "Sonos", "Triad", "Araknis", "Luma", "Qolsys", "Samsung",
           "Ubiquiti", "Episode", "Strong", "Binary", "Cindev", "Josh.ai", "Snap One"]
_SERVICE_TYPES = ["prewire", "networking", "audio", "lighting", "security", "shades",
                  "theater", "mounting", "wiring", "automation", "surveillance"]
_LOCATIONS = ["Vail", "Beaver Creek", "Edwards", "Avon", "Eagle", "Singletree", "Cordillera",
              "Minturn", "Arrowhead", "Cordillera", "Bachelor Gulch"]

_TAG_PATTERNS = (
    [(n, n.lower()) for n in _CLIENT_NAMES] +
    [(b, b.lower()) for b in _BRANDS] +
    [(s, s.lower()) for s in _SERVICE_TYPES] +
    [(l, l.lower()) for l in _LOCATIONS]
)


def extract_tags(title: str, body: str, folder: str) -> list[str]:
    blob = f"{title} {body} {folder}".lower()
    return [tag for display, tag in _TAG_PATTERNS if display.lower() in blob]


# ── Subcategory extraction ─────────────────────────────────────────────────────

def extract_subcategory(title: str, folder: str, category: str) -> str:
    """Extract a meaningful subcategory from title/folder."""
    # Client name from folder
    for client in _CLIENT_NAMES:
        if client.lower() in folder.lower() or client.lower() in title.lower():
            return client.lower()
    # Brand from title
    for brand in _BRANDS:
        if brand.lower() in title.lower():
            return brand.lower()
    # Fallback: folder name slug
    slug = re.sub(r"[^a-z0-9]+", "_", folder.lower()).strip("_")
    return slug[:40] if slug else "general"


# ── Cortex POST ───────────────────────────────────────────────────────────────

def post_to_cortex(payload: dict, dry_run: bool = False) -> bool:
    """POST a memory to Cortex. Returns True on success."""
    if dry_run:
        logger.info("DRY-RUN would POST: %s / %s", payload.get("category"), payload.get("title", "")[:60])
        return True
    try:
        resp = httpx.post(f"{CORTEX_URL}/remember", json=payload, timeout=5.0)
        return resp.status_code == 200
    except Exception as exc:
        logger.warning("cortex_post_failed: %s — %s", payload.get("title", "")[:40], exc)
        return False


# ── Notes ingestion ───────────────────────────────────────────────────────────

def ingest_notes(dry_run: bool) -> dict[str, Any]:
    """Read notes_index.json and ingest each valid note into Cortex."""
    stats: dict[str, Any] = {
        "total": 0, "ingested": 0, "skipped_empty": 0,
        "skipped_duplicate": 0, "failed": 0, "by_category": defaultdict(int),
    }

    if not NOTES_INDEX.is_file():
        logger.warning("notes_index.json not found — run notes_indexer.py --index first")
        return stats

    data = json.loads(NOTES_INDEX.read_text(encoding="utf-8"))
    notes = data.get("notes", [])
    seen = _load_seen()
    new_seen: set[str] = set()

    for note in notes:
        stats["total"] += 1
        title = (note.get("title") or "").strip()
        # notes_index.json stores "body" (full text, added in Y2) or
        # falls back to "summary" (one-line) for older index files.
        body = (note.get("body") or note.get("summary") or "").strip()
        folder = (note.get("folder") or "").strip()
        note_id = note.get("note_id", "")

        # Skip empty / stub notes
        if len(body) < 20 and not note.get("attachment_count", 0):
            stats["skipped_empty"] += 1
            continue
        if title.lower() in ("new note", "") and len(body) < 20:
            stats["skipped_empty"] += 1
            continue

        # Dedupe by content hash
        h = _content_hash(body)
        if h in seen:
            stats["skipped_duplicate"] += 1
            continue
        new_seen.add(h)

        category, importance = categorize(title, body, folder)
        subcategory = extract_subcategory(title, folder, category)
        tags = extract_tags(title, body, folder)
        if not tags:
            tags = ["apple_notes"]
        else:
            tags = list(dict.fromkeys(tags + ["apple_notes"]))[:10]

        # Redact sensitive values from content logged
        safe_content = body[:2000]
        if category == "access_codes":
            # Don't log actual codes to report — Cortex stores them, report won't
            tags = list(dict.fromkeys(tags + ["sensitive"]))

        payload = {
            "category": category,
            "subcategory": subcategory,
            "title": title or f"Untitled — {folder}",
            "content": safe_content,
            "source": f"apple_notes/{folder}",
            "confidence": 0.8,
            "importance": importance,
            "tags": tags,
            "metadata": {
                "note_id": note_id,
                "folder": folder,
                "created_at": note.get("created_at", ""),
                "modified_at": note.get("modified_at", ""),
                "attachment_count": note.get("attachment_count", 0),
                "ingested_from": "notes_to_cortex.py",
            },
        }

        if post_to_cortex(payload, dry_run):
            stats["ingested"] += 1
            stats["by_category"][category] += 1
            seen.add(h)
        else:
            stats["failed"] += 1

    if not dry_run:
        _save_seen(seen | new_seen)

    return stats


# ── Knowledge bootstrap ───────────────────────────────────────────────────────

def _read_md_front_matter(path: Path) -> dict[str, str]:
    """Extract YAML front matter from a markdown file."""
    text = path.read_text(encoding="utf-8", errors="ignore")
    m = re.match(r"^---\n(.+?)\n---", text, re.DOTALL)
    if not m:
        return {}
    out: dict[str, str] = {}
    for line in m.group(1).splitlines():
        if ":" in line:
            k, _, v = line.partition(":")
            out[k.strip()] = v.strip().strip('"').strip("'")
    return out


def ingest_products(dry_run: bool) -> dict[str, Any]:
    """Ingest knowledge/products/*.md into Cortex as product_reference entries."""
    stats: dict[str, Any] = {"total": 0, "ingested": 0, "failed": 0}
    products_dir = KNOWLEDGE_DIR / "products"
    if not products_dir.is_dir():
        return stats

    seen = _load_seen()
    new_seen: set[str] = set()

    for md_path in sorted(products_dir.glob("*.md")):
        stats["total"] += 1
        try:
            content = md_path.read_text(encoding="utf-8", errors="ignore")
            fm = _read_md_front_matter(md_path)
            name = fm.get("name") or md_path.stem.replace("-", " ").title()
            vendor = fm.get("vendor", "")
            sku = fm.get("sku", "")
            title = f"{name} ({sku})" if sku else name

            h = _content_hash(content)
            if h in seen:
                continue
            new_seen.add(h)

            tags = ["product_reference"]
            if vendor:
                tags.append(re.sub(r"\s.*", "", vendor).lower())  # first word of vendor
            for brand in _BRANDS:
                if brand.lower() in content.lower():
                    tags.append(brand.lower())

            payload = {
                "category": "product_reference",
                "subcategory": re.sub(r"\s.*", "", vendor).lower() if vendor else "general",
                "title": title,
                "content": content[:3000],
                "source": f"knowledge/products/{md_path.name}",
                "confidence": 0.95,
                "importance": 7,
                "tags": list(dict.fromkeys(tags))[:10],
                "metadata": {
                    "sku": sku,
                    "vendor": vendor,
                    "msrp": fm.get("msrp", ""),
                    "file": str(md_path.relative_to(REPO_ROOT)),
                    "ingested_from": "notes_to_cortex.py",
                },
            }

            if post_to_cortex(payload, dry_run):
                stats["ingested"] += 1
                seen.add(h)
                logger.info("  product: %s", title[:60])
            else:
                stats["failed"] += 1

        except Exception as exc:
            logger.warning("product ingest error %s: %s", md_path.name, exc)
            stats["failed"] += 1

    if not dry_run:
        _save_seen(seen | new_seen)
    return stats


def ingest_system_shells(dry_run: bool) -> dict[str, Any]:
    """Ingest knowledge/projects/*/system_shell.md into Cortex."""
    stats: dict[str, Any] = {"total": 0, "ingested": 0, "failed": 0, "projects": []}
    projects_dir = KNOWLEDGE_DIR / "projects"
    if not projects_dir.is_dir():
        return stats

    seen = _load_seen()
    new_seen: set[str] = set()

    for project_dir in sorted(projects_dir.iterdir()):
        shell_path = project_dir / "system_shell.md"
        if not shell_path.is_file():
            continue
        stats["total"] += 1
        try:
            content = shell_path.read_text(encoding="utf-8", errors="ignore")
            client_slug = project_dir.name
            client_display = client_slug.replace("_", " ").title()
            # First line heading is the proper name
            m = re.search(r"^#\s+(.+)$", content, re.MULTILINE)
            title = m.group(1).strip() if m else f"System Shell — {client_display}"

            h = _content_hash(content)
            if h in seen:
                continue
            new_seen.add(h)

            tags = ["system_shell", client_slug.lower()]
            for loc in _LOCATIONS:
                if loc.lower() in content.lower():
                    tags.append(loc.lower())

            payload = {
                "category": "system_shell",
                "subcategory": client_slug.lower(),
                "title": title,
                "content": content[:4000],
                "source": f"knowledge/projects/{client_slug}/system_shell.md",
                "confidence": 0.95,
                "importance": 9,
                "tags": list(dict.fromkeys(tags))[:10],
                "metadata": {
                    "client_slug": client_slug,
                    "file": str(shell_path.relative_to(REPO_ROOT)),
                    "ingested_from": "notes_to_cortex.py",
                },
            }

            if post_to_cortex(payload, dry_run):
                stats["ingested"] += 1
                stats["projects"].append(client_slug)
                seen.add(h)
                logger.info("  system_shell: %s", title[:60])
            else:
                stats["failed"] += 1

        except Exception as exc:
            logger.warning("shell ingest error %s: %s", project_dir.name, exc)
            stats["failed"] += 1

    if not dry_run:
        _save_seen(seen | new_seen)
    return stats


# ── Generic markdown ingest ───────────────────────────────────────────────────

def ingest_md_files(
    paths: list[Path],
    category: str,
    importance: int,
    dry_run: bool,
    subcategory_fn=None,
    extra_tags: list[str] | None = None,
    source_prefix: str = "knowledge",
) -> dict[str, Any]:
    """Ingest a list of markdown files into Cortex under the given category.

    subcategory_fn(path) → str, called per file if supplied.
    """
    stats: dict[str, Any] = {"total": len(paths), "ingested": 0, "failed": 0, "titles": []}
    seen = _load_seen()
    new_seen: set[str] = set()

    for md_path in paths:
        try:
            content = md_path.read_text(encoding="utf-8", errors="ignore").strip()
            if len(content) < 30:
                continue

            h = _content_hash(content)
            if h in seen:
                continue
            new_seen.add(h)

            # Title: first # heading or filename
            m = re.search(r"^#\s+(.+)$", content, re.MULTILINE)
            title = m.group(1).strip() if m else md_path.stem.replace("-", " ").replace("_", " ").title()

            subcategory = subcategory_fn(md_path) if subcategory_fn else md_path.stem.replace("-", "_")
            tags = list(dict.fromkeys(
                [category, subcategory] + (extra_tags or []) + extract_tags(title, content, "")
            ))[:12]

            source = f"{source_prefix}/{md_path.relative_to(REPO_ROOT)}"
            payload = {
                "category": category,
                "subcategory": subcategory,
                "title": title,
                "content": content[:4000],
                "source": source,
                "confidence": 0.9,
                "importance": importance,
                "tags": tags,
                "metadata": {
                    "file": str(md_path.relative_to(REPO_ROOT)),
                    "size_chars": len(content),
                    "ingested_from": "notes_to_cortex.py",
                },
            }

            if post_to_cortex(payload, dry_run):
                stats["ingested"] += 1
                stats["titles"].append(title[:60])
                seen.add(h)
                logger.info("  %s: %s", category, title[:60])
            else:
                stats["failed"] += 1

        except Exception as exc:
            logger.warning("ingest_md error %s: %s", md_path.name, exc)
            stats["failed"] += 1

    if not dry_run:
        _save_seen(seen | new_seen)
    return stats


def _collect_md(base: Path, pattern: str = "*.md", recursive: bool = False) -> list[Path]:
    """Collect markdown files matching pattern, skipping README."""
    if not base.exists():
        return []
    files = list(base.rglob(pattern) if recursive else base.glob(pattern))
    return sorted(p for p in files if p.is_file() and p.name.lower() != "readme.md")


