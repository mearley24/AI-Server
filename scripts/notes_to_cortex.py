# ── knowledge/product_reference.md rollup ────────────────────────────────────

def build_product_reference_md() -> int:
    """Build knowledge/product_reference.md from individual product files."""
    products_dir = KNOWLEDGE_DIR / "products"
    if not products_dir.is_dir():
        return 0

    by_vendor: dict[str, list[dict]] = defaultdict(list)

    for md_path in sorted(products_dir.glob("*.md")):
        try:
            fm = _read_md_front_matter(md_path)
            if not fm:
                continue
            vendor = fm.get("vendor", "Other")
            # Strip parenthetical from vendor: "Control4 (Snap One)" → "Control4"
            vendor_clean = re.sub(r"\s*\(.*\)", "", vendor).strip() or "Other"
            by_vendor[vendor_clean].append({
                "name": fm.get("name", md_path.stem),
                "sku": fm.get("sku", ""),
                "msrp": fm.get("msrp", ""),
                "notes": fm.get("notes", ""),
            })
        except Exception:
            pass

    total = sum(len(v) for v in by_vendor.values())
    if total == 0:
        return 0

    lines = [
        "# Product Reference Library",
        "",
        f"*Auto-generated by notes_to_cortex.py — {datetime.now().strftime('%Y-%m-%d')}*",
        f"*{total} products across {len(by_vendor)} vendors*",
        "",
    ]

    for vendor in sorted(by_vendor):
        lines.append(f"## {vendor}")
        lines.append("")
        lines.append("| Model | SKU | MSRP | Notes |")
        lines.append("|---|---|---|---|")
        for p in sorted(by_vendor[vendor], key=lambda x: x["name"]):
            lines.append(f"| {p['name']} | {p['sku']} | {p['msrp']} | {p['notes']} |")
        lines.append("")

    PRODUCT_REF_MD.write_text("\n".join(lines), encoding="utf-8")
    logger.info("Wrote knowledge/product_reference.md (%d products)", total)
    return total


# ── Ingest report ─────────────────────────────────────────────────────────────

def write_report(
    notes_stats: dict,
    prod_stats: dict,
    shell_stats: dict,
    all_knowledge_stats: list[tuple[str, dict]],
    prod_count: int,
    procedures_ingested: list[str],
    dry_run: bool,
) -> None:
    now = datetime.now(timezone.utc).strftime("%Y-%m-%d")

    # Totals
    total_ingested = (
        notes_stats["ingested"]
        + prod_stats["ingested"]
        + shell_stats["ingested"]
        + sum(s["ingested"] for _, s in all_knowledge_stats)
    )
    total_failed = (
        notes_stats["failed"]
        + prod_stats["failed"]
        + shell_stats["failed"]
        + sum(s["failed"] for _, s in all_knowledge_stats)
    )

    lines = [
        f"# Notes Ingest Report — {now}",
        "",
        *(["*(dry-run — nothing was written to Cortex)*", ""] if dry_run else []),
        "## Summary",
        "",
        f"**Total ingested to Cortex this run: {total_ingested}** ({total_failed} failed)",
        "",
        "| Source | Files | Ingested | Skipped (dup/empty) | Failed |",
        "|---|---|---|---|---|",
        (
            f"| Apple Notes | {notes_stats['total']} "
            f"| {notes_stats['ingested']} "
            f"| {notes_stats['skipped_empty'] + notes_stats['skipped_duplicate']} "
            f"| {notes_stats['failed']} |"
        ),
        (
            f"| Products (knowledge/products/) | {prod_stats['total']} "
            f"| {prod_stats['ingested']} "
            f"| {prod_stats['total'] - prod_stats['ingested'] - prod_stats['failed']} "
            f"| {prod_stats['failed']} |"
        ),
        (
            f"| System shells (knowledge/projects/) | {shell_stats['total']} "
            f"| {shell_stats['ingested']} "
            f"| {shell_stats['total'] - shell_stats['ingested'] - shell_stats['failed']} "
            f"| {shell_stats['failed']} |"
        ),
    ]

    for label, s in all_knowledge_stats:
        skipped = s["total"] - s["ingested"] - s["failed"]
        lines.append(
            f"| {label} | {s['total']} "
            f"| {s['ingested']} "
            f"| {skipped} "
            f"| {s['failed']} |"
        )
    lines.append("")

    # Notes categories
    if notes_stats.get("by_category"):
        lines += ["## Apple Notes — Categories Breakdown", ""]
        for cat, cnt in sorted(notes_stats["by_category"].items(), key=lambda x: -x[1]):
            lines.append(f"- **{cat}**: {cnt}")
        lines.append("")

    # System shells
    if shell_stats.get("projects"):
        lines += ["## System Shells Ingested", ""]
        for slug in shell_stats["projects"]:
            lines.append(f"- `{slug}`: `knowledge/projects/{slug}/system_shell.md`")
        lines.append("")

    # Procedures
    lines += ["## Procedures Extracted", ""]
    if procedures_ingested:
        for p in procedures_ingested:
            lines.append(f"- {p}")
    else:
        lines.append("*(none new — all already in Cortex)*")
    lines.append("")
    lines.append("Procedure library: `knowledge/procedures/`")
    lines.append("")

    # Product reference
    lines += [
        "## Product Reference",
        "",
        f"- `{prod_count}` entries in `knowledge/product_reference.md`",
        f"- `{prod_stats['ingested']}` new product records ingested to Cortex",
        "",
    ]

    # Other knowledge ingested
    lines += ["## Knowledge Base Entries Ingested", ""]
    for label, s in all_knowledge_stats:
        if s["ingested"] > 0:
            lines.append(f"**{label}** — {s['ingested']} entries:")
            for t in s.get("titles", [])[:5]:
                lines.append(f"  - {t}")
            if len(s.get("titles", [])) > 5:
                lines.append(f"  - *(+{len(s['titles']) - 5} more)*")
    lines.append("")

    # Apple Notes JXA status
    lines += [
        "## Apple Notes JXA Status",
        "",
        "The Apple Notes JXA interface (`osascript`) timed out during this run.",
        "This is expected when Notes.app has a large iCloud library syncing.",
        "",
        "**To re-run when Notes is accessible:**",
        "```zsh",
        "python3 integrations/apple_notes/notes_indexer.py --index --no-llm",
        "python3 scripts/notes_to_cortex.py",
        "```",
        "",
        "The script is idempotent — duplicate content is skipped via SHA-256 hashing.",
        "Running again when Notes is available will add note content without re-ingesting",
        "anything already in Cortex.",
        "",
    ]

    lines += [
        "## Notes Flagged for Manual Review",
        "",
        "*(No Apple Notes available this run)*",
        "",
    ]

    INGEST_REPORT.write_text("\n".join(lines), encoding="utf-8")
    logger.info("Wrote %s", INGEST_REPORT)


# ── Main ──────────────────────────────────────────────────────────────────────

def main() -> int:
    ap = argparse.ArgumentParser(description="Ingest Apple Notes + full knowledge base into Cortex")
    ap.add_argument("--dry-run", action="store_true", help="Categorize and log without POSTing")
    ap.add_argument("--skip-knowledge", action="store_true", help="Skip knowledge/ bootstrap")
    args = ap.parse_args()

    dry_run = args.dry_run
    if dry_run:
        logger.info("DRY-RUN mode — no Cortex writes")

    # Ensure procedures dir exists
    ensure_procedures_dir()

    # Build product reference rollup
    prod_count = build_product_reference_md()

    # ── Step 1: Apple Notes ────────────────────────────────────────────────────
    logger.info("=== Step 1: Ingest Apple Notes ===")
    notes_stats = ingest_notes(dry_run)
    logger.info(
        "Notes: total=%d ingested=%d skipped_empty=%d skipped_dup=%d failed=%d",
        notes_stats["total"], notes_stats["ingested"],
        notes_stats["skipped_empty"], notes_stats["skipped_duplicate"],
        notes_stats["failed"],
    )

    prod_stats: dict[str, Any] = {"total": 0, "ingested": 0, "failed": 0}
    shell_stats: dict[str, Any] = {"total": 0, "ingested": 0, "failed": 0, "projects": []}
    all_knowledge_stats: list[tuple[str, dict]] = []

    if not args.skip_knowledge:
        # ── Step 2: Products ───────────────────────────────────────────────────
        logger.info("=== Step 2: Product references ===")
        prod_stats = ingest_products(dry_run)
        logger.info("Products: total=%d ingested=%d", prod_stats["total"], prod_stats["ingested"])

        # ── Step 3: System shells ──────────────────────────────────────────────
        logger.info("=== Step 3: System shells ===")
        shell_stats = ingest_system_shells(dry_run)
        logger.info("Shells: total=%d ingested=%d projects=%s",
                    shell_stats["total"], shell_stats["ingested"], shell_stats["projects"])

        # ── Step 4: Procedures ─────────────────────────────────────────────────
        logger.info("=== Step 4: Procedures ===")
        proc_files = _collect_md(PROCEDURES_DIR, "*.md")
        proc_stats = ingest_md_files(
            proc_files, "work_procedure", 8, dry_run,
            subcategory_fn=lambda p: p.stem.replace("-", "_"),
        )
        all_knowledge_stats.append(("Procedures (knowledge/procedures/)", proc_stats))
        logger.info("Procedures: total=%d ingested=%d", proc_stats["total"], proc_stats["ingested"])

        # ── Step 5: Operations runbook ─────────────────────────────────────────
        logger.info("=== Step 5: Operations runbook ===")
        ops_path = KNOWLEDGE_DIR / "operations-runbook.md"
        ops_stats = ingest_md_files(
            [ops_path] if ops_path.exists() else [],
            "work_procedure", 8, dry_run,
            extra_tags=["operations", "runbook", "bob"],
        )
        all_knowledge_stats.append(("Operations Runbook", ops_stats))

        # ── Step 6: Hardware playbooks ─────────────────────────────────────────
        logger.info("=== Step 6: Hardware playbooks ===")
        hw_files = _collect_md(KNOWLEDGE_DIR / "hardware", "*.md")
        hw_stats = ingest_md_files(
            hw_files, "work_procedure", 8, dry_run,
            extra_tags=["hardware", "install"],
        )
        all_knowledge_stats.append(("Hardware playbooks (knowledge/hardware/)", hw_stats))
        logger.info("Hardware: total=%d ingested=%d", hw_stats["total"], hw_stats["ingested"])

        # ── Step 7: Network docs ───────────────────────────────────────────────
        logger.info("=== Step 7: Network docs ===")
        net_files = _collect_md(KNOWLEDGE_DIR / "network", "*.md")
        net_stats = ingest_md_files(
            net_files, "work_procedure", 7, dry_run,
            extra_tags=["networking", "ip", "vlan"],
        )
        all_knowledge_stats.append(("Network docs (knowledge/network/)", net_stats))

        # ── Step 8: SOW blocks → proposal_template ────────────────────────────
        logger.info("=== Step 8: SOW blocks ===")
        sow_files = _collect_md(KNOWLEDGE_DIR / "sow-blocks", "*.md")
        sow_stats = ingest_md_files(
            sow_files, "proposal_template", 7, dry_run,
            subcategory_fn=lambda p: p.stem.replace("-", "_"),
            extra_tags=["scope_of_work", "proposal"],
        )
        all_knowledge_stats.append(("SOW blocks (knowledge/sow-blocks/)", sow_stats))
        logger.info("SOW: total=%d ingested=%d", sow_stats["total"], sow_stats["ingested"])

        # ── Step 9: Proposal library ───────────────────────────────────────────
        logger.info("=== Step 9: Proposal library ===")
        prop_lib_files = _collect_md(KNOWLEDGE_DIR / "proposal_library", "*.md", recursive=True)
        prop_lib_stats = ingest_md_files(
            prop_lib_files, "proposal_template", 7, dry_run,
            extra_tags=["proposal_library", "room_config"],
        )
        all_knowledge_stats.append(("Proposal library (knowledge/proposal_library/)", prop_lib_stats))
        logger.info("Proposal lib: total=%d ingested=%d",
                    prop_lib_stats["total"], prop_lib_stats["ingested"])

        # ── Step 10: Incidents → troubleshooting ──────────────────────────────
        logger.info("=== Step 10: Incidents ===")
        inc_files = _collect_md(KNOWLEDGE_DIR / "incidents", "*.md")
        inc_stats = ingest_md_files(
            inc_files, "troubleshooting", 8, dry_run,
            extra_tags=["incident", "root_cause", "docker"],
        )
        all_knowledge_stats.append(("Incidents (knowledge/incidents/)", inc_stats))

        # ── Step 11: Reports → business_operations ────────────────────────────
        logger.info("=== Step 11: Reports ===")
        rep_files = _collect_md(KNOWLEDGE_DIR / "reports", "*.md")
        rep_stats = ingest_md_files(
            rep_files, "business_operations", 6, dry_run,
            extra_tags=["report", "analysis"],
        )
        all_knowledge_stats.append(("Reports (knowledge/reports/)", rep_stats))

        # ── Step 12: Cortex learnings → meta_learning ─────────────────────────
        logger.info("=== Step 12: Cortex learnings ===")
        learn_path = KNOWLEDGE_DIR / "cortex" / "learnings.md"
        learn_stats = ingest_md_files(
            [learn_path] if learn_path.exists() else [],
            "meta_learning", 7, dry_run,
            extra_tags=["cortex", "learnings", "continuous_learning"],
        )
        all_knowledge_stats.append(("Cortex learnings", learn_stats))

        # ── Step 13: Agent runbooks → infrastructure ──────────────────────────
        logger.info("=== Step 13: Agent runbooks ===")
        agent_files = _collect_md(KNOWLEDGE_DIR / "agents", "*.md")
        agent_stats = ingest_md_files(
            agent_files, "infrastructure", 6, dry_run,
            extra_tags=["agent", "runbook", "bob"],
        )
        all_knowledge_stats.append(("Agent runbooks (knowledge/agents/)", agent_stats))

        # ── Step 14: Proposals (equipment rollups) ─────────────────────────────
        logger.info("=== Step 14: Equipment rollups ===")
        proposals_files = _collect_md(KNOWLEDGE_DIR / "proposals", "*.md")
        prop_stats = ingest_md_files(
            proposals_files, "system_shell", 8, dry_run,
            extra_tags=["equipment_rollup", "proposal"],
        )
        all_knowledge_stats.append(("Equipment rollups (knowledge/proposals/)", prop_stats))

    # ── Report ─────────────────────────────────────────────────────────────────
    procedures_ingested: list[str] = []
    for _, s in all_knowledge_stats:
        if any("procedure" in t for t in s.get("titles", [])):
            procedures_ingested.extend(s.get("titles", []))
    # Fallback: collect procedure titles from proc_stats if present
    for label, s in all_knowledge_stats:
        if "Procedures" in label:
            procedures_ingested = s.get("titles", [])

    write_report(
        notes_stats, prod_stats, shell_stats, all_knowledge_stats,
        prod_count, procedures_ingested, dry_run,
    )

    # Cortex health
    try:
        resp = httpx.get(f"{CORTEX_URL}/health", timeout=3.0)
        if resp.status_code == 200:
            health = resp.json()
            memories = health.get("memories", {})
            logger.info(
                "Cortex health: total_memories=%s active_goals=%s",
                memories.get("total", "?"),
                memories.get("active_goals", "?"),
            )
    except Exception as exc:
        logger.warning("Cortex health check failed: %s", exc)

    total_ingested = (
        notes_stats["ingested"] + prod_stats["ingested"] + shell_stats["ingested"]
        + sum(s["ingested"] for _, s in all_knowledge_stats)
    )
    logger.info("=== Done — %d total entries ingested to Cortex ===", total_ingested)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
